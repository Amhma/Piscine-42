int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int unsigned	i;
	int				res;

	i = 0;
	res = 0;
	while ((*(s1 + i) || *(s2 + i)) && i < n)
	{
		if (*(s1 + i) != *(s2 + i))
		{
			res = *(s1 + i) - *(s2 + i);
			return (res);
		}
		i++;
	}
	return (res);
}
