int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str && *(str + i))
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			if (!(str[i] >= 'a' && str[i] <= 'z'))
				return (0);
		}
		i++;
	}
	return (1);
}
