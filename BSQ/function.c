int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	res;

	i = 0;
	s = 1;
	res = 0;
	if (str)
	{	
		while (str[i] != '\0' && ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' ')))
			i++;
		while ((str[i] != '\0') && (str[i] == '-' || str[i] == '+'))
		{
			if (str[i] == '-')
				s *= -1;
			i++;
		}
		while ((str[i] != '\0') && (str[i] >= '0' && str[i] <= '9'))
			res = ((res * 10) + (str[i++] - '0'));
	}
	return (res * s);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
		i++;
	return (i);
}
